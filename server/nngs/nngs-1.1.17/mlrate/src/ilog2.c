unsigned int
ilog2(register unsigned long x)
{
  register unsigned int bit = 0;

  if (x & 0xffffff00)
  {
    if (x & 0xffff0000)		
      if (x & 0xff000000)
      {
	x >>= 24;		
	bit = 24;
      }
      else
      {
	x >>= 16;
	bit = 16;
      }
    else
      if (x & 0x0000ff00)
      {
	x >>= 8;
	bit = 8;
      }
  }
  return bit + bitpostab[x];
}
