	public Bbox getBbox(BugBoard b)
	{
		 Bbox box = new Bbox();
		 int celldis = -1;
		 for(int lim=size()-1; lim>=0; lim--)
		 {
			 BugCell c = elementAt(lim);
			 int x = b.cellToX(c.col,c.row);
			 int y = b.cellToY(c.col,c.row);
			 box.addPoint(x,y);		
			 if(celldis==-1)
			 {
				 int x1 = b.cellToX(c.col,c.row+1)-x;
				 int y1 = b.cellToY(c.col,c.row+1)-y;
				 int cell = box.cellsize = (int)Math.sqrt(x1*x1+y1*y1);
				 celldis = cell/2;
				 
			 }
		 }
		 return box;
	}
	// draw a bug as a single entity.  This gets used to draw bugs in the game log.
	public void drawChip(Graphics gc, DrawingObject c, int size, int posx, int posy, String msg) 
	{	BugBoard b = (BugBoard)((BugViewer)c).getBoard();
		Bbox box = getBbox(b);
		double xspan = (box.right-box.left);
		double yspan = (box.bottom-box.top);
		int dx = (box.left+box.right)/2;
		int dy = (box.top+box.bottom)/2;
		double cellspan = Math.max(xspan,yspan)/box.cellsize;
		int psize = (int)( size/Math.max(3,cellspan));
		double scale = (Math.max(yspan,xspan)/(psize*1.3));
		for(int lim=size()-1;lim>=0;lim--)
		{
			BugCell cell = elementAt(lim);
			int xx = b.cellToX(cell.col,cell.row);
			int yy = b.cellToY(cell.col,cell.row);
			int x = (int)((xx - dx)/scale);
			int y = (int)(-(yy- dy)/scale);
			top.drawChip(gc,c,psize,posx+x,posy+y,null);
		}
	}
