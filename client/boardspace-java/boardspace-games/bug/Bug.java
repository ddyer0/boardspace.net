	/** calculating a unique bug identity.
	 *  the mark and sweep algorithm encounters cells in a fixed order, so the set of cells encountered
	 *  given a particular starting point and a particular direction will be consistent.  Considering 
	 *  all 6 directions as "first" and all N cells in the bug as the starting point, there are N*6 
	 *  different hashes possible for the bug, and among those one will be the smallest.  That is
	 *  the identity of the bug.   This recognizes chiral differences of the same bug as different,
	 *  which is appropriate for the game.
	 */
	int masterIndex = 0;
	
	// Starting at some elent of the bug, and some direction as first direction, calculate
	// a digest with the random number table.  
	@SuppressWarnings("unused")
	private long findNoFlipIdentity(BugCell seed,int firstDirection,int sweepCounter)
	{	long id = 0;
		if(seed.sweep_counter!=sweepCounter)
		{	seed.sweep_counter = sweepCounter;
			id = masterSequence.elementAt(masterIndex++);
			for(int direction=firstDirection;direction<BugBoard.CELL_FULL_TURN+firstDirection; direction++)
			{
				BugCell next = seed.exitTo(direction);
				if(next!=null)
				{	
					if(next.topChip()==top)
					{
					id ^= findNoFlipIdentity(next,firstDirection);
					}
					else { masterIndex++; }
				}
				else { masterIndex++; }
			}
		}
		return id;
	}
	// starting with a seed element, calculate all 6 possible digests
	private long findNoFlipIdentity(BugCell seed,int sweepCounter)
	{
		masterIndex = 0;
		long id = findNoFlipIdentity(seed,0);
		for(int dir=1;dir<BugBoard.CELL_FULL_TURN;dir++)
		{
			sweepCounter++;
			masterIndex = 0;
			id = Math.min(findNoFlipIdentity(seed,dir),id);
		}
		return id;
	}
	// find the minimum of all possible digests for this bug.  This version considers
	// mirror images to be different.
	long findNoFlipIdentity(int sweepCounter)
	{
		long least = findNoFlipIdentity(elementAt(0),sweepCounter);
		for(int i=1;i<size();i++)
		{	BugCell seed = elementAt(i);
			long newid = findNoFlipIdentity(seed,sweepCounter);
			least = Math.min(least,newid);
		}
		return least;
	}
	//
	// find the digest for this shape.  This version considers mirror images to be the same
	//
	long findFlipIdentity(BugBoard b)
	{	// collect the set of distances from each cell to each of the others
		LStack distances = b.tempLstack();
		for(int lim = size()-1; lim>=0; lim--)
		{	BugCell from = elementAt(lim);
			CC fromClass = from.getConnectionClass();
			int fromOrd = fromClass.ordinal();
			for(int prev = 0; prev<lim; prev++)
			{
				BugCell to = elementAt(prev);
				CC toClass = to.getConnectionClass();
				long distance = b.distanceSquared(from,to);
				int toOrd =toClass.ordinal();
				int ocode = fromOrd>toOrd ? fromOrd<<4|toOrd : toOrd<<4|fromOrd;
				long code = (distance << 8) | ocode;
				distances.push(code);
			}
		}
		distances.sort();
		long id = 0;
		for(int sz = distances.size()-1; sz>=0; sz--)
		{
			id ^= masterSequence.elementAt(sz)*distances.elementAt(sz);
		}

		b.returnTempL(distances);
		return id;
	}
	
	long findConnectIdentity(BugBoard b)
	{	// collect the set of distances from each cell to each of the others
		LStack distances = b.tempLstack();
		for(int lim = size()-1; lim>=0; lim--)
		{	BugCell from = elementAt(lim);
			CC fromClass = from.getConnectionClass();
			int fromOrd = fromClass.ordinal();
			distances.push(fromOrd);
		}
		distances.sort();
		long id = 0;
		for(int sz = distances.size()-1; sz>=0; sz--)
		{
			id ^= masterSequence.elementAt(sz)*distances.elementAt(sz);
		}

		b.returnTempL(distances);
		return id;
	}
	
	long findDoubleConnectIdentity(BugBoard b)
	{	// collect the set of distances from each cell to each of the others
		LStack distances = b.tempLstack();
		for(int lim = size()-1; lim>=0; lim--)
		{	BugCell from = elementAt(lim);
			CC fromClass = from.getConnectionClass();
			int fromOrd = fromClass.ordinal();
			for(int prev = 0; prev<lim; prev++)
			{
				BugCell to = elementAt(prev);
				CC toClass = to.getConnectionClass();
				int toOrd =toClass.ordinal();
				int ocode = fromOrd>toOrd ? fromOrd<<4|toOrd : toOrd<<4|fromOrd;
				distances.push(ocode);
			}
		}
		distances.sort();
		long id = 0;
		for(int sz = distances.size()-1; sz>=0; sz--)
		{
			id ^= masterSequence.elementAt(sz)*distances.elementAt(sz);
		}

		b.returnTempL(distances);
		return id;
	}

