class LongRandomSequence extends LStack
{
	Random r = null;
	LongRandomSequence(long seed)
	{
		r = new Random(seed);
	}
	
	public long elementAt(int n)
	{
		while(size()<=n) { push(r.nextLong()); }
		return super.elementAt(n);
	}
}
class BugStack extends OStack<Bug> implements Digestable
{
	public Bug[] newComponentArray(int sz) {
		return new Bug[sz];
	}
	public long Digest(Random r) {
		long v = 0;
		for(int lim=size()-1; lim>=0; lim--) { v ^= elementAt(lim).Digest(r);}
		return v;
	}
	public void deepCopyFrom(BugBoard b,BugStack other)
	{
		clear();
		for(int i=0;i<other.size();i++)
		{
			push(b.copy(other.elementAt(i)));
		}
	}
}
